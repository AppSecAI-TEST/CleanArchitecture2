package com.cleanarchitecture.shishkin.common.content;

public interface IBaseColumns {

    /**
     * The android unique ID for a row.
     * <P>Type: TEXT </P>
     */
    String _ID = "_id";

    /**
     * The unique RowId for a row.
     * <P>Type: TEXT </P>
     */
    String RowId = "RowId";

    /**
     * The count of rows in a directory.
     * <P>Type: INTEGER</P>
     */
    String _COUNT = "_count";

}